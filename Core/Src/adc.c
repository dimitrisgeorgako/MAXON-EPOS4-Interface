/* ADC StartUp function */
void Enable_ADC_Conversions(ADC_HandleTypeDef *hadc)
{
	HAL_ADC_Start_DMA(&hadc1,(uint32_t *)adc_buf, ADC_BUF_LEN);
}


/* ADC Callback function */
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *hadc)
{
	adc_counter++;

	/* Normal Averaging */
	if(adc_counter < adc_counter_const)
	{
		adcVal1 += adc_buf[0];	// Steering Linear
		adcVal2 += adc_buf[1];	// Spare Analog
	}
	/* ADC Calculations */
	else
	{
		/* Steering Linear Sensor voltage and increment readings */
		linear_voltage = 3.25*((adcVal1/4095.0)/adc_counter);
		dv.steering_linear_mm = ((LINEAR_ZERO - ((adcVal1/adc_counter)))/4095.0)*75.2;
		dv.steering_linear_mm = (dv.steering_linear_mm*span);

		/* Transformed in degrees of steering wheel */
		dv.steering_deg = dv.steering_linear_mm * 4.0933;

		/* Reset all variables for next "cycle" */
		adcVal1 = 0;
		adcVal2 = 0;
		adc_counter = 0;
	}
}

